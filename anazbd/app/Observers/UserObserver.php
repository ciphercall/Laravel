    /**
     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        $config = SiteConfig::first();
        if($config->is_cashback_enabled ?? false){
            CashReturn::create([
                'user_id' => $user->id,
                'amount' => $config->cashback_amount,
            ]);
        }else{
            CashReturn::create([
                'user_id' => $user->id,
                'amount' => 0,
            ]);
        }
    }

